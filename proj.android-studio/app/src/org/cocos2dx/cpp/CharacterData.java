public class CharacterData {

    private int no;
    private String name;
    private String detail;
    private String image_stand;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getImage_stand() {
        return image_stand;
    }

    public void setImage_stand(String image_stand) {
        this.image_stand = image_stand;
    }
}
